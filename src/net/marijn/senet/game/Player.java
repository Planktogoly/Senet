package net.marijn.senet.game;

public class Player {
	
	private String name;
	
	private String pion;
	
	public Player(String name) {
		this.name = name;
	}
	
	public String getPion() {
		return pion;
	}
	
	public void setPion(String pion) {
		this.pion = pion;
	}
	
	public String getName() {
		return name;
	}
	

}
